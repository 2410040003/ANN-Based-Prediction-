import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

data = pd.read_csv('reflection_coefficient_dataset.csv')

print(data.head())

X = data[['ZL', 'Z0']]   # Inputs
y = data['Gamma']        # Output

from sklearn.preprocessing import StandardScaler

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

from sklearn.model_selection import train_test_split

X_train, X_test, y_train, y_test = train_test_split(
    X_scaled, y, test_size=0.2, random_state=42
)

from sklearn.neural_network import MLPRegressor

model = MLPRegressor(
    hidden_layer_sizes=(20, 10),
    activation='relu',
    solver='adam',
    max_iter=3000,
    random_state=42
)

model.fit(X_train, y_train)

y_pred = model.predict(X_test)

print("Sample Predictions:", y_pred[:5])

results = pd.DataFrame({
    'Actual': y_test.values,
    'Predicted': y_pred
})

print(results.head())

from sklearn.metrics import mean_squared_error

mse = mean_squared_error(y_test, y_pred)
print("Mean Squared Error:", mse)

from sklearn.metrics import r2_score

r2 = r2_score(y_test, y_pred)
print("R2 Score:", r2)

plt.figure()
plt.scatter(y_test, y_pred)
plt.plot([-1,1], [-1,1])  # Ideal line
plt.xlabel("Actual Gamma")
plt.ylabel("Predicted Gamma")
plt.title("Actual vs Predicted Reflection Coefficient")
plt.show()

# Example input: ZL = 100, Z0 = 50
new_input = [[100, 50]]

# Apply same scaling
new_input_scaled = scaler.transform(new_input)

prediction = model.predict(new_input_scaled)

print("Predicted Gamma:", prediction)

